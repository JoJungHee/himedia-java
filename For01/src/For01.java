
public class For01 {
	public static void main(String[] args) {
		// 문제1) 1부터 10까지 1씩 증가하는 값을 출력하시오.
		/*
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		*/
		
		// 문제2) 10부터 1까지 1씩 감소하는 값을 출력하시오.
		/*
		for(int i=10; i>=1; i--) {
			System.out.println(i);
		}
		*/
				
		// 문제3) 1000부터 1200까지 8씩 증가하는 값을 출력하시오.
		/*
		for(int i=1000; i<=1200; i+=8) {
			System.out.println(i);
		}
		*/
		
		// 문제4) 3000부터 2000까지 12씩 감소하는 값을 출력하시오.
		for(int i=3000; i>=2000; i-=12) {
			System.out.println(i);
		}
	}

}
